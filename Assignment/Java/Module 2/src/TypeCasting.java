public class TypeCasting {
    public static void main(String[] args){
        short num1=45;
        double num2= 105661.25454545;

        //type casting
        byte num3=(byte)num1;
        int num4=(int)num2;
        float num5=(float)num2;
        long num6=(long)num2;

        System.out.println(num3);
        System.out.println(num4);
        System.out.println(num5);
        System.out.println(num6);
    }
}
